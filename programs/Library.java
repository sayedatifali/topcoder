	public int documentAccess(String[] records, String[] userGroups, String[] roomRights)
	{
		int i, j, N, access = 0;
		String doc, room, uGroup;
		StringTokenizer st;
		sUserGroups = new HashSet<String>();
		sRoomRights = new HashSet<String>();
		sDocs = new HashSet<String>();
		
		N = records.length;
		
		for (i = 0; i < userGroups.length; i++) {	
			sUserGroups.add(userGroups[i]);
		}
		for (i = 0; i < roomRights.length; i++) {	
			sRoomRights.add(roomRights[i]);
		}
		
		for (i = 0; i < N; i++) {
		
			st = new StringTokenizer(records[i]);
			doc = st.nextToken();
			room = st.nextToken();
			uGroup = st.nextToken();
			
			if (sUserGroups.contains(uGroup) && sRoomRights.contains(room) && !sDocs.contains(doc)) {
				access++;
				sDocs.add(doc);
			}
		}
		
		return access;
		
	}
	
