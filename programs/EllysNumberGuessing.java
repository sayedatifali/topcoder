	public void putInMap(int key) {
	
		int val;
		
		if (key <= 0 || key > MAX)
			return;
	
		if (guessCount.containsKey(key)) {
		
			val = guessCount.get(key);
			val++;
			guessCount.put(key, val);		
		} else {
			guessCount.put(key, 1);		
		}
	
	
	}
	
