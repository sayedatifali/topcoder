	public int maxProfit(int[] cityValues, String[] roads)
	{
		int i, j;
		
		value = 0;
		maxValues = Integer.MIN_VALUE;
		N = cityValues.length;
		marked = new boolean[N][1024];
		
		dfs(roads, cityValues, 0, cityValues[0]);
		
		return maxValues;
	}
	
	// Implemented dfs using a stack as the recursive call will throw a StackOverflowError
	
	public void dfs(String [] roads, int [] cityValues, int i, int val) {
	
		Deque<Data> stack = new ArrayDeque<Data>();
		Data data;
		
		data = new Data(i, val);
		
		stack.addFirst(data);
		
		while (!stack.isEmpty()) {
		
			data = stack.removeFirst();
			i = data.i;
			val = data.val;
			
			if (marked[i][val])
				continue;
			
			marked[i][val] = true;
			
			maxValues = Math.max(maxValues, val);
						
			for (int j = 0; j < N; j++) {
			
				if (roads[i].charAt(j) == 'Y') {					
					data = new Data(j, val ^ cityValues[j]);
					stack.addFirst(data);
				}
			}
		}	
	}
	
