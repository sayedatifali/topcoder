	public int choose(String[] lyrics)
	{
		int N;
		int songs;
		boolean [] marked;
		ArrayList<Integer> adj[];
		int size[];
		int minSongs = Integer.MAX_VALUE;
		
		N = lyrics.length;
		songs = lyrics[0].length();
		size = new int[songs];
		
		adj = (ArrayList<Integer> []) new ArrayList[songs];
		
		for (int i = 0; i < songs; i++)
			adj[i] = new ArrayList<Integer>();
		
		marked = new boolean[N];
		
		
		for (int i = 0; i < N; i++) {
			for (int j = 0; j < songs; j++) {
				if (lyrics[i].charAt(j) == 'Y') {
					adj[j].add(i);
					size[j]++;
				}
			}
		}
		boolean mark;
		int temp;
		
		for (int i = 1; i < (1 << songs); i++) {
		
			marked = new boolean[N];
			mark = true;
		//	temp = Integer.MAX_VALUE;
			temp = 0;
			
			for (int j = 0; j < songs; j++) {
			
				if ( (i & (1 << j)) != 0) {
				
					for (int x: adj[j])
						marked[x] = true;				
					temp++;
				}
			
			}
			
			
			for (int j = 0; j < N; j++) {
				if (!marked[j]) {
					mark = false;
					break;
				}
			}
			
			if (mark && temp < minSongs)
				minSongs = temp;
		
		}
		
		
		return minSongs;
		
	}
	
