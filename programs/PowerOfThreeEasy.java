	public boolean takeNextStep(int x1, int y1, int xHome, int yHome, int n) {
	
		int step;
		boolean reach = false;
		
		if (x1 == xHome && y1 == yHome)
			return true;
			
		if (x1 > xHome || y1 > yHome)
			return false;
			
		step = (int) Math.pow(3, n);
		
		reach = takeNextStep(x1 + step, y1, xHome, yHome, n + 1);
		
		if (reach)
			return reach;
			
		reach = takeNextStep(x1, y1 + step, xHome, yHome, n + 1);
		
		return (reach || false);
	
	}
	
